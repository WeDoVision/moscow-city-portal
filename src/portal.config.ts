/**
 * Конфиг портала — сердце темплейта.
 *
 * Каждый портал Whitewill (Москва-Сити, аренда, особняки, район N) — это
 * один такой конфиг: брендинг, тема, скоуп объектов (комплексы/фильтры),
 * копирайт и SEO. Генератор порталов (в т.ч. будущий AI-генератор) должен
 * эмитить именно этот объект — весь остальной код универсален.
 */

export type TowerConfig = {
  /** complex_id в API whitewill.ru */
  id: number;
  slug: string;
  name: string;
  /** короткая подпись для карточки башни */
  tagline: string;
};

export type PortalConfig = {
  brand: {
    /** название портала, идёт в логотип и метаданные */
    name: string;
    /** разбивка логотипа на две части для типографики */
    logo: [string, string];
    legalName: string;
    phone: string;
    phoneHref: string;
    email: string;
  };
  /** прод-домен портала (для canonical, sitemap, OG) */
  siteUrl: string;
  seo: {
    title: string;
    description: string;
    keywords: string[];
    locale: string;
  };
  hero: {
    kicker: string;
    title: string;
    subtitle: string;
    image: string;
  };
  /** скоуп портала: какие комплексы показываем */
  towers: TowerConfig[];
  /** дефолтная сортировка каталога */
  defaultSort: string;
  faq: { q: string; a: string }[];
  about: {
    title: string;
    paragraphs: string[];
    stats: { value: string; label: string }[];
  };
};

